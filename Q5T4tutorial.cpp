#include<iostream>
using namespace std ;
int main()
{
    int number ;
    cout<<"Enter the number : ";
    cin>>number;

    if(number>0)
    {
        cout<<"Positive Number "<<endl;
    }
    else
    {
        cout<<"Negative Number"<<endl;
    }
    return 0;
}