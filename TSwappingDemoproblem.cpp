//Question : demo for Swapping. 
//Write a program to swap the values of two numbers. 
#include<iostream>
using namespace std;
int main()
{
    int n1 = 5 ;
    int n2 = 6;
    int temp ;

    temp = n1 ;
    n1 = n2 ;
    n2 = temp ;

    cout<<"After Swap "<<n1<<" "<<n2;

    return 0;



}