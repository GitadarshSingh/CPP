#include<iostream>
#include<bits/stdc++.h>
using namespace std;
int main()
{
    char x = 128;
    x++;
    cout<<(int)x<<endl;


    char b = 127;
    b++;
    cout<<(int)b<<endl;

    char c = -129;
    c++;
    cout<<(int)c<<endl;


    char d = -128;
    d++;
    cout<<(int)d<<endl;

    int e = INT__MAX;
    e++;
    cout<<e<<endl;



    return 0;
}